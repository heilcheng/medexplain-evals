# Function to skip certain members during documentation generation
def skip_member(app, what, name, obj, skip, options):
    """Skip private members and test methods"""
    if name.startswith('_') and name != '__init__':
        return True
    if name.startswith('test_'):
        return True
    return skip

def setup(app):
    """Sphinx app setup function"""
    app.connect('autodoc-skip-member', skip_member)